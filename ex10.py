print ("I am 6'2\" tall")
# using double quotes to print 
# use Escape character \ for the double quote "

print ('I am 6\'2" tall')
# using single quotes to print 
# use excape character \ for the single quote '

tabby_cat = "\tI'm tabbed in"
# \t is an escape character for horizontal tab

persian_cat = "I'm split\non a line"
# \n escape character for new line

backslash_cat = "I'm \\ a \\ cat."
# use \ escape charcter for \

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print (tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)


# Syntax
# \t ---> tab
# \n ---> new line
# \\ ---> escape backslash
# \" ---> escape double quotes
# \' ---> escape single quotes





